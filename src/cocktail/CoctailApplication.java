    private void addFruitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFruitActionPerformed
      try{                                     
     String FruitNmae = NameListOfFruites.getSelectedItem().toString();
     int volume = Integer.parseInt(volumeFruitTextField.getText());
     int r = Integer.parseInt(RsystemColorFruit.getText());
     int g = Integer.parseInt(GsystemColorFruit.getText());
     int b = Integer.parseInt(BsystemColorFruit.getText());
     int calories=0;
     if(NameListOfFruites.getSelectedIndex()==0)
          calories=volume/2;
      if(NameListOfFruites.getSelectedIndex()==1)
         calories=volume*3/5;
        
     if(NameListOfFruites.getSelectedIndex()==2)
         calories=volume/5;
         
     if(NameListOfFruites.getSelectedIndex()==3)
         calories=volume/5;
    if(NameListOfFruites.getSelectedIndex()==4)
         calories=volume/2;
    caloriesFruitTextField.setText(""+calories);
    
    Fruits fruits = new Fruits(volume, new Color(r, g, b), FruitNmae, calories);
   
    
        
        displayIngredientList();
       try {
           blunder.add(fruits);
           ingrediant.add(fruits);
       } catch (BlenderOverFlowException ex) {
           JOptionPane.showMessageDialog(this, "Blunder is full !!");
       }
      }catch(Exception e){
        JOptionPane.showMessageDialog(this, e.toString(), "Error maybe in your input data", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_addFruitActionPerformed


    private void ShowFileInformationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowFileInformationActionPerformed
        // TODO add your handling code here:
         ArrayList<Ingredients>ingrediant1=null;
         Blunder b=null;
         ArrayList<Cup>cups1=null;
        try{
            
             ObjectInputStream objectInputStream = new ObjectInputStream(new FileInputStream("ingredientWithBlenderAndCups.dat"));
                   
            try{
            String str ="";
          
           while (true){
                 ingrediant1 =(ArrayList) objectInputStream.readObject();
               b=(Blunder)objectInputStream.readObject();
             cups1=(ArrayList) objectInputStream.readObject();
               str="Our Blender: "+"\n"+b.getInfo()+"\n"+"Ingredient : ";
               for (int i = 0; i < ingrediant1.size(); i++) {
                   str+=ingrediant1.get(i).getInfo()+"\n";
               }
               str+="\n"+"Cups :";
                for(int i =0;i<cups1.size();i++){
                str+=cups1.get(i).getInfo()+"\n";
                }
              showLoadedObject.setText(str);
             }
             
             }
             catch(EOFException eof ){
            JOptionPane.showMessageDialog(this,"end of file !!");
             }
            objectInputStream.close();
   }
  catch(IOException ioe){
 JOptionPane.showMessageDialog(this,"Something wrong in your file  !!");
   }
   catch(ClassNotFoundException classNotFoundException){
  JOptionPane.showMessageDialog(this,"Something wrong in your class  !!");
   }                 

           
    }//GEN-LAST:event_ShowFileInformationActionPerformed

